/*

! ============= Mongooose =================
tool to work with MongoDB more easily in Node.js


! ============= MongoDB =================
the database itself, raw, flexible.

*/

// ===============================
// Real-Life Analogy:
// MongoDB is like a blank notebook (freeform).
// Mongoose is like a pre-designed form (structured, consistent).
// ===============================

/*
| Feature           | MongoDB                        | Mongoose                             |
|------------------|--------------------------------|--------------------------------------|
| Type             | Database                       | ODM (library)                        |
| Structure        | Schema-less                    | Schema-based                         |
| Validation       | Manual (custom logic)          | Built-in validation                  |
| Use case         | Flexible data, raw control     | Structured app, consistency needed   |
*/