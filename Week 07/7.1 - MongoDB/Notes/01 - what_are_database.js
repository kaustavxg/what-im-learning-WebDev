//! What are databases
/*
Databases are organized collections of data that are structured to enable efficient storage, retrieval, and management of information.


# Types:

1. Relational Database - Use tables to store data and relationships between data (e.g., MySQL, PostgreSQL).

2. NoSQL Databases -  Designed for more flexible data models and often used for big data or real-time web applications (e.g., MongoDB).

3. Graph Database -  Store data in nodes and edges to represent relationships (e.g., Neo4j).

4. Vector Databases: Used in ML to store data in the form of embeddings



*/

