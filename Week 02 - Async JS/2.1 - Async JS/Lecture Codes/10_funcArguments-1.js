function sum(a, b) {
    return a + b;
  }
  
  function multiply(a, b) {
    return a * b;
  }
  
  function subtract(a, b) {
    return a - b;
  }
  
  function divide(a, b) {
    return a / b;
  }

const ans = sum(1, 2)
const ans2 = subtract(1, 2)
const ans3 = multiply(1, 2)
const ans4 = divide(1, 2)

console.log(ans) // 1+2 = 3
console.log(ans2) // 1-2 = -1
console.log(ans3) // 1*2 = 2
console.log(ans4) // 1/2 = 0.5